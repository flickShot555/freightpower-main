export function t(language, key, fallback) {
  const lang = normalizeLanguage(language);

  const translated = DICTS?.[lang]?.[key];
  if (translated != null) return translated;

  // English: allow natural fallbacks.
  if (lang === 'English') return fallback ?? DICTS?.English?.[key] ?? key;

  // Non-English: do NOT show raw English fallback strings.
  // Instead, return a localized placeholder so untranslated UI is obvious and no English leaks through.
  try {
    if (typeof window !== 'undefined') {
      window.__FP_MISSING_I18N_KEYS__ = window.__FP_MISSING_I18N_KEYS__ || {};
      window.__FP_MISSING_I18N_KEYS__[lang] = window.__FP_MISSING_I18N_KEYS__[lang] || new Set();
      window.__FP_MISSING_I18N_KEYS__[lang].add(String(key));
    }
  } catch {
    // ignore
  }

  if (typeof console !== 'undefined' && console?.warn) {
    console.warn(`[i18n] Missing key for ${lang}: ${key}`);
  }

  if (lang === 'Spanish') return `⟦Falta traducción: ${key}⟧`;
  if (lang === 'Arabic') return `⟦ترجمة مفقودة: ${key}⟧`;
  return `⟦Missing translation: ${key}⟧`;
}
